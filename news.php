        <p>Вышла Mageia 3 Beta 1.
            Это первый релиз, содержащий в себе некоторые изменения в инсталляторе. Например, доступна опция
            использования GRUB2 и т.д. <br>
            При установке системы с DVD-дисков можно установить проприетарные драйвера и прошивки, для этого нужно лишь
            включить nonfree ветку. <br>
        </p>

        <p>
            В блоге Mageia отдельно отмечено, что команда контроля качества ожидает баг-репорты пользователей. По этому,
            если вы заметите какой-либо баг
            (пусть даже не значительный), не поленитесь отписаться о нем в багзиллу Mageia. Если по какой-либо причине у
            вас нет возможности это сделать
            (не владеете английский языком, нет времени на оформление и т.д. и т.п.), то хотя бы создайте тему с
            описанием бага здесь - на форуме нашего сообщества,
            а мы уже отпишемся в багзиллу дистрибутива. Это важно, помогите сделать наш любимый дистрибутив еще лучше.
        </p>

